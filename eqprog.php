                            <div class="heading-underline1"></div>
                            <p style="margin-top:20px;">Ending all discrimination against women and girls is not only a basic human right, it’s crucial for sustainable future; it’s proven that empowering women and girls helps economic growth and development. </p>
                            <p style="margin-top:-40px;">Bong University Graduates has made gender equality central to its work and we’ve seen remarkable progress in the past years of existing. There are more girls glad to be in school now compared to some years ago, and most regions have reached gender parity in primary education.</p>
                            <p style="margin-top:-40px;">While the general destruction of school infrastructure, limited availability of trained teachers and inappropriate teaching materials common to conflict and crisis settings can undermine the quality of education for all children, there are also specific gender implications. </p>
                            <p style="margin-top:-40px;">Available teachers are less likely to be trained in gender-sensitive approaches and existing teaching practices and learning materials can reinforce gender stereotypes that significantly disadvantage girls. </p>
                        <p style="margin-top:-40px;">Furthermore, a limited number of either female or male teachers can result in a lack of role models, and students may become less motivated to participate in class.</p>
                    <p style="margin-top:-40px;">The Bong University Graduates remain unbending to supporting gender equality in all aspects of lives.</p>    
